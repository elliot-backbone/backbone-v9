/**
 * Get actions summary for operator
 * @param {Array<Object>} actions 
 * @returns {Object}
 */
export function getActionsSummary(actions) {
  const bySource = {
    ISSUE: 0,
    PREISSUE: 0,
    GOAL: 0,
    MANUAL: 0
  };
  
  for (const action of actions) {
    const sourceType = action.sources?.[0]?.sourceType || 'MANUAL';
    bySource[sourceType] = (bySource[sourceType] || 0) + 1;
  }
  
  const avgImpact = actions.length > 0
    ? actions.reduce((sum, a) => sum + (a.expectedNetImpact || 0), 0) / actions.length
    : 0;
  
  return {
    total: actions.length,
    bySource,
    avgExpectedNetImpact: Math.round(avgImpact * 100) / 100,
    topAction: actions[0]?.title || null,
    companies: [...new Set(actions.map(a => a.entityRef?.id))].filter(Boolean).length
  };
}

/**
 * Filter actions by source type
 * @param {Array<Object>} actions 
 * @param {string} sourceType 
 * @returns {Array<Object>}
 */
export function filterBySource(actions, sourceType) {
  return actions.filter(a => a.sources?.[0]?.sourceType === sourceType);
}

/**
 * Filter actions by company
 * @param {Array<Object>} actions 
 * @param {string} companyId 
 * @returns {Array<Object>}
 */
export function filterByCompany(actions, companyId) {
  return actions.filter(a => a.entityRef?.id === companyId);
}

