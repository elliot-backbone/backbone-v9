/**
 * Extract today's actions from engine output.
 * Returns ONLY top N actions - pure action surface.
 * 
 * Phase 3.2: Actions come from actionRanker, already ranked by expected net impact.
 * 
 * @param {Object} engineOutput - Full engine output
 * @param {Object} [options] - Configuration options
 * @param {number} [options.limit=10] - Maximum actions to return
 * @returns {Array<Object>} - Action objects for operator
 */
export function getTodayActions(engineOutput, options = {}) {
  const limit = options.limit || 10;
  
  // Phase 3.2: Pull from ranked actions (primary artifact)
  if (engineOutput?.actions && Array.isArray(engineOutput.actions)) {
    return engineOutput.actions.slice(0, limit);
  }
  
  // Fallback: todayActions if present
  if (engineOutput?.todayActions && Array.isArray(engineOutput.todayActions)) {
    return engineOutput.todayActions.slice(0, limit);
  }
  
  return [];
}

